class City {
  city: string;
  county: string;
  country: string;
  mayorEmail: string;
  constructor(
    city: string,
    county: string,
    country: string,
    mayorEmail: string
  ) {
    this.city = city;
    this.country = country;
    this.county = county;
    this.mayorEmail = mayorEmail;
  }
}
